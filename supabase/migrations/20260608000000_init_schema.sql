-- Krostio V2: Database Initialization and Migration Script
-- Designed for Supabase (PostgreSQL with built-in Auth schemas)

-- 1. Create Profiles table (extending auth.users)
CREATE TABLE IF NOT EXISTS public.profiles (
  id UUID REFERENCES auth.users(id) ON DELETE CASCADE PRIMARY KEY,
  email TEXT NOT NULL,
  full_name TEXT,
  avatar_url TEXT DEFAULT 'https://images.unsplash.com/photo-1534528741775-53994a69daeb?w=100&h=100&fit=crop&crop=faces',
  tier TEXT DEFAULT 'Free' CHECK (tier IN ('Free', 'Pro', 'Pro+')),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  phone_number TEXT,
  monthly_income_goal NUMERIC DEFAULT 5000,
  tax_filing_status TEXT DEFAULT 'Single'
);

-- Enable Row Level Security (RLS) for profiles
ALTER TABLE public.profiles ENABLE ROW LEVEL SECURITY;

-- Drop existing policies if any, then create cleanly
DO $$ 
BEGIN
    DROP POLICY IF EXISTS "Allow users to view their own profile" ON public.profiles;
    DROP POLICY IF EXISTS "Allow users to update their own profile" ON public.profiles;
    DROP POLICY IF EXISTS "Allow profile insertion on sign up" ON public.profiles;
EXCEPTION
    WHEN undefined_object THEN NULL;
END $$;

CREATE POLICY "Allow users to view their own profile"
  ON public.profiles FOR SELECT
  USING (auth.uid() = id);

CREATE POLICY "Allow users to update their own profile"
  ON public.profiles FOR UPDATE
  USING (auth.uid() = id);

CREATE POLICY "Allow profile insertion on sign up"
  ON public.profiles FOR INSERT
  WITH CHECK (true);


-- 2. Create Earnings table (sync'd platform records)
CREATE TABLE IF NOT EXISTS public.earnings (
  id TEXT PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  platform TEXT NOT NULL,
  amount NUMERIC NOT NULL,
  date TEXT NOT NULL, -- format: YYYY-MM (chronological monthly ledger key)
  verified_by TEXT DEFAULT 'Argyle',
  argyle_timestamp TIMESTAMPTZ DEFAULT NOW(),
  status TEXT CHECK (status IN ('Growing', 'Stable', 'Seasonal', 'Declining')),
  category TEXT CHECK (category IN ('rideshare', 'delivery', 'freelance_tech', 'freelance_creative')),
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Enable RLS for earnings
ALTER TABLE public.earnings ENABLE ROW LEVEL SECURITY;

DO $$ 
BEGIN
